$( document ).ready(function() {
$("#carouselposterthumb").mouseover(function () {
  $("#carouselposterthumbpink").show();document.getElementById('carouselposter').style.backgroundColor = "magenta";
});

$("#carouselposterthumbpink").mouseout(function () {
  $("#carouselposterthumbpink").hide();document.getElementById('style').style.backgroundColor = "red";
});












});